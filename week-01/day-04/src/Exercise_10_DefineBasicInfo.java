public class Exercise_10_DefineBasicInfo {
    public static void main(String[] args) {
        // Define several things as a variable then print their values
        // Your name as a string
        // Your age as an integer
        // Your height in meters as a double
        // Whether you are married or not as a boolean

        String myName = "Balazs Varga";
        int myAge = 26;
        double myHeight = 1.93;
        boolean isMarried = false;

        System.out.println("My name is: " + myName);
        System.out.println("I'm " + myAge + " years old");
        System.out.println("I'm " + myHeight + " cm tall");
        System.out.println("Am I married? " + isMarried);
    }
}
